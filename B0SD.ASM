PUBLIC	SD_R,SD_W

;EXTRN	CPM_RECORD,CPM_TRACK,DMA_ADDR	; From BxDISK module


;------ RAM DISK OPERATIONS -------------
; READ ONE SECTOR FROM RAM DISK
SD_R:	CALL	SD_SEEK
	LHLD	DMA_ADDR
	XCHG
	LXI	h,128
	JMP	CmdRead

; WRITE ONE SECTOR TO RAM DISK
SD_W:	CALL	SD_SEEK
	LHLD	DMA_ADDR
	XCHG
	LXI	h,128
	JMP	CmdWrite

SD_I::
	MVI	A,80H
	STA	PALM_CNTRL
	LXI	H,FONT
	MVI	D,0
	CALL	CmdOpenDelete
	LXI	D,0D800H
	LXI	H,2048
	CALL	CmdRead
	MVI	A,0C0h
	STA	PALM_CNTRL

	LXI	H,FNAME
	MVI	D,0
	CALL	CmdOpenDelete
	LXI	D,BUF
	LXI	H,16
	CALL	CmdRead
	RET

SD_SEEK:
	LDA	CPM_TRACK
	;ADI	2
	MOV	L,A
	MVI	H,0
	DAD	H
	DAD	H
	DAD	H
	MOV	D,H
	MOV	E,L
	DAD	H
	DAD	H
	DAD	D ; hl = trk*1024*5/256

	MVI	D,0
	MOV	E,H
	MOV	H,L
	MOV	L,D
	MOV	B,D
	PUSH	D
	XCHG
	LHLD	DATA_ADDR
	DAD	D
	POP	D
	LXI	B,128
	LDA	CPM_RECORD
	dcr	a
	jz	@cmp11
@cmp10:	DAD	B
	DCR	A
	JNZ	@cmp10
@cmp11:
	MVI	B,0
	JMP	CmdSeekGetSize

BUF:	DS	16
DATA_ADDR	EQU	BUF+0Ah
FNAME:	DB	'CPM/A.FDI',0
FONT:	DB	'CPM/8X16ENG.FNT',0


;	END
